void slow_print(const char* text, int speed) {
    for (int i = 0; text[i] != '\0'; i++) {
        printf("%c", text[i]);
        fflush(stdout);
        usleep(speed);
    }
    printf("\n");
}

void ask(const char* question, const char* answer, const char* reflection) {
    char input[100];
    while (1) {
        printf("\n\033[1;37m%s\033[0m\n> ", question);
        scanf("%s", input);
        
        if (strcasecmp(input, answer) == 0) {
            if (strlen(reflection) > 0) {
                printf("\033[0;90m");
                slow_print(reflection, 50000);
                printf("\033[0m");
                sleep(1);
            }
            break;
        } else {
            printf("\033[0;31m... не, опитайте отново ...\033[0m\n");
        }
    }
}

